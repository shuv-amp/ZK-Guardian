// Types

export interface TransactionResult {
    success: boolean;
    txHash?: string;
    blockNumber?: number;
    gasUsed?: bigint;
    error?: string;
}

export interface GasEstimate {
    gasLimit: bigint;
    maxFeePerGas: bigint;
    maxPriorityFeePerGas: bigint;
    estimatedCost: string; // In POL/MATIC
}

export interface ContractAddresses {
    verifier: string;
    audit: string;
    registry: string;
}

// Provider & Wallet Management

let providerInstance: ethers.JsonRpcProvider | null = null;
let walletInstance: ethers.Wallet | null = null;

/**
 * Get the Polygon Amoy provider (singleton)
 */
export function getProvider(): ethers.JsonRpcProvider {
    if (!providerInstance) {
        if (!env.POLYGON_AMOY_RPC) {
            throw new Error('POLYGON_AMOY_RPC not configured');
        }
        providerInstance = new ethers.JsonRpcProvider(env.POLYGON_AMOY_RPC);
        logger.info({ rpc: env.POLYGON_AMOY_RPC }, 'Blockchain provider initialized');
    }
    return providerInstance;
}

/**
 * Get the gateway wallet for signing transactions
 */
export async function getWallet(): Promise<ethers.Wallet> {
    if (!walletInstance) {
        const privateKey = await getGatewayPrivateKey();
        const provider = getProvider();
        walletInstance = new ethers.Wallet(privateKey, provider);
        logger.info({ address: walletInstance.address }, 'Gateway wallet initialized');
    }
    return walletInstance;
}

/**
 * Get wallet address without exposing private key
 */
export async function getWalletAddress(): Promise<string> {
    const wallet = await getWallet();
    return wallet.address;
}

/**
 * Check wallet balance
 */
export async function getWalletBalance(): Promise<{ balance: string; isLow: boolean }> {
    const wallet = await getWallet();
    const provider = getProvider();
    const balance = await provider.getBalance(wallet.address);
    const balanceInMatic = ethers.formatEther(balance);
    const isLow = parseFloat(balanceInMatic) < 0.1;

    if (isLow) {
        logger.warn({ address: wallet.address, balance: balanceInMatic }, 'Wallet balance low');
    }

    return { balance: balanceInMatic, isLow };
}

