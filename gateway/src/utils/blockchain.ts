// Consent Revocation Helpers

/**
 * Check if a consent has been revoked
 */
export async function isConsentRevoked(consentHash: string): Promise<boolean> {
    const contract = await getRevocationContract();
    return contract.isRevoked(consentHash);
}

/**
 * Check multiple consents at once
 */
export async function checkConsentsRevoked(consentHashes: string[]): Promise<boolean[]> {
    const contract = await getRevocationContract();
    return contract.checkConsents(consentHashes);
}

/**
 * Revoke a consent on-chain
 */
export async function revokeConsent(
    consentHash: string,
    reason: string
): Promise<TransactionResult> {
    const contract = await getRevocationContract();
    return sendTransaction(contract, 'revokeConsent', [consentHash, reason]);
}

// Health Check

/**
 * Check blockchain connectivity
 */
export async function checkBlockchainHealth(): Promise<{
    connected: boolean;
    blockNumber?: number;
    chainId?: string;
    walletBalance?: string;
    error?: string;
}> {
    try {
        const provider = getProvider();
        const [blockNumber, network] = await Promise.all([
            provider.getBlockNumber(),
            provider.getNetwork()
        ]);

        const { balance } = await getWalletBalance();

        return {
            connected: true,
            blockNumber,
            chainId: network.chainId.toString(),
            walletBalance: balance
        };
    } catch (error: any) {
        return {
            connected: false,
            error: error.message
        };
    }
}
