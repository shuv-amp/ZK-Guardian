// Transaction Utilities

/**
 * Estimate gas for a transaction
 */
export async function estimateGas(
    contract: Contract,
    method: string,
    args: any[]
): Promise<GasEstimate> {
    const provider = getProvider();

    try {
        const gasLimit = await contract[method].estimateGas(...args);
        const feeData = await provider.getFeeData();

        const maxFeePerGas = feeData.maxFeePerGas || ethers.parseUnits('50', 'gwei');
        const maxPriorityFeePerGas = feeData.maxPriorityFeePerGas || ethers.parseUnits('2', 'gwei');

        const estimatedCost = ethers.formatEther(gasLimit * maxFeePerGas);

        return {
            gasLimit,
            maxFeePerGas,
            maxPriorityFeePerGas,
            estimatedCost
        };
    } catch (error: any) {
        logger.error({ error, method }, 'Gas estimation failed');
        throw error;
    }
}

/**
 * Send a transaction with automatic retry and gas management
 */
export async function sendTransaction(
    contract: Contract,
    method: string,
    args: any[],
    options: {
        maxRetries?: number;
        confirmations?: number;
        gasMultiplier?: number;
    } = {}
): Promise<TransactionResult> {
    const {
        maxRetries = 3,
        confirmations = 1,
        gasMultiplier = 1.2
    } = options;

    const startTime = Date.now();
    let lastError: Error | null = null;

    for (let attempt = 1; attempt <= maxRetries; attempt++) {
        try {
            // Estimate gas
            const gasEstimate = await estimateGas(contract, method, args);
            const gasLimit = BigInt(Math.ceil(Number(gasEstimate.gasLimit) * gasMultiplier));

            logger.info({
                method,
                attempt,
                gasLimit: gasLimit.toString(),
                estimatedCost: gasEstimate.estimatedCost
            }, 'Sending transaction');

            // Send transaction
            const tx: ContractTransactionResponse = await contract[method](...args, {
                gasLimit,
                maxFeePerGas: gasEstimate.maxFeePerGas,
                maxPriorityFeePerGas: gasEstimate.maxPriorityFeePerGas
            });

            // Wait for confirmation
            const receipt: TransactionReceipt | null = await tx.wait(confirmations);

            if (!receipt) {
                throw new Error('Transaction receipt is null');
            }

            const duration = Date.now() - startTime;

            // Record metrics
            prometheusMetrics.blockchainTransactions.inc({
                method,
                status: 'success'
            });
            prometheusMetrics.blockchainLatency.observe({ method }, duration / 1000);

            logger.info({
                txHash: receipt.hash,
                blockNumber: receipt.blockNumber,
                gasUsed: receipt.gasUsed.toString(),
                duration
            }, 'Transaction confirmed');

            return {
                success: true,
                txHash: receipt.hash,
                blockNumber: receipt.blockNumber,
                gasUsed: receipt.gasUsed
            };

        } catch (error: any) {
            lastError = error;

            // Check if retryable
            const isRetryable = isRetryableError(error);

            logger.warn({
                method,
                attempt,
                error: error.message,
                isRetryable
            }, 'Transaction attempt failed');

            if (!isRetryable || attempt === maxRetries) {
                break;
            }

            // Exponential backoff
            await sleep(Math.pow(2, attempt) * 1000);
        }
    }

    // All retries failed
    prometheusMetrics.blockchainTransactions.inc({
        method,
        status: 'failed'
    });

    return {
        success: false,
        error: lastError?.message || 'Transaction failed'
    };
}

/**
 * Check if an error is retryable
 */
function isRetryableError(error: any): boolean {
    const retryableMessages = [
        'nonce too low',
        'replacement fee too low',
        'transaction underpriced',
        'network congestion',
        'timeout',
        'ETIMEDOUT',
        'ECONNRESET'
    ];

    const message = error?.message?.toLowerCase() || '';
    return retryableMessages.some(m => message.includes(m.toLowerCase()));
}

/**
 * Sleep utility
 */
function sleep(ms: number): Promise<void> {
    return new Promise(resolve => setTimeout(resolve, ms));
}

// Event Listening

/**
 * Listen for AccessAudited events
 */
export async function onAccessAudited(
    callback: (event: {
        proofHash: string;
        accessHash: string;
        policyMatch: string;
        timestamp: bigint;
        txHash: string;
    }) => void
): Promise<() => void> {
    const contract = await getAuditContract();

    const listener = (
        proofHash: string,
        accessHash: string,
        policyMatch: string,
        timestamp: bigint,
        event: any
    ) => {
        callback({
            proofHash,
            accessHash,
            policyMatch,
            timestamp,
            txHash: event.log.transactionHash
        });
    };

    contract.on('AccessAudited', listener);

    logger.info('Subscribed to AccessAudited events');

    // Return unsubscribe function
    return () => {
        contract.off('AccessAudited', listener);
        logger.info('Unsubscribed from AccessAudited events');
    };
}

